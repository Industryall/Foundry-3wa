<h2 class="aboutTitle">Qu'est-ce que Foundry ?</h2>

<p class="aboutP">
  Foundry est une interface d'aide à la création de personnages pour JDR, ou jeu de rôle dans lequel
  les participants conçoivent ensemble une fiction par l’interprétation de rôles et par la
  narration, dans le cadre de contraintes de jeu qu’ils s’imposent.
</p>

<p class="aboutP">
  Dans sa forme traditionnelle, ce jeu rassemble les joueurs durant la partie autour d'une table,
  avec communément pour accessoires des dés et feuilles de papier.
</p>

<p class="aboutP">
  Chez nous, vous pourrez tout à fait commencer par concevoir votre personnage en lui attribuant des
  caractéristiques et des statistiques pour démarrer votre aventure.
</p>

<h2 class="aboutTitle">Qui peut y jouer ? Et quels sont les rôles des participants ?</h2>

<p class="aboutP">Tous JDR se composent d'un MJ (maître de jeu) et de joueurs.</p>
<p class="aboutP">
  Le MJ est le narrateur de l'aventure, il décidera arbitrairement de l'évolution de la partie en
  offrant la possibilité aux joueurs de décider de leur sort.
</p>